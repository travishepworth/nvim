return {
	"stevearc/conform.nvim",
	opts = {},
	config = function()
		local conform = require("conform")
		conform.setup({
			formatters_by_ft = {
				javascript = { "prettier" },
				typescript = { "prettier" },
				javascriptreact = { "prettier" },
				typescriptreact = { "prettier" },
				css = { "prettier" },
        scss = {"prettier"},
				html = { "prettier" },
				ejs = { "prettier" },
				json = { "prettier" },
				markdown = { "prettier" },
				graphql = { "prettier" },
        yaml = {"prettier"},
				lua = { "stylua" },
				python = { "black" },
				ruby = { "rubocop" },
				bash = { "shfmt" },
				c = { "clang-format" },
				cpp = { "clang-format" },
				md = { "mdformat" },
        nix = {"nixfmt"},
			},
		})
		vim.keymap.set({ "n", "v" }, "<leader>cf", function()
			conform.format({
				lsp_fallback = true,
				async = false,
				timeout_ms = 1000,
			})
		end, { desc = "Format file or range (in visual mode)" })
	end,
}
